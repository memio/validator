namespace Memio\Validator\Violation;

use Memio\Validator\Violation;

class SomeViolation implements Violation
{
    private $message;

    public function __construct(string $message)
    {
        $this->message = $message;
    }

    public function getMessage(): string
    {
        return $this->message;
    }
}
